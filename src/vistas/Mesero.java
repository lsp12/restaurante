    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTable2.getSelectedRow() != -1) {
            String codigo = (String) table.getValueAt(jTable2.getSelectedRow(), 1);

            // Lo imprimimos en pantalla
            Conexion conn = new Conexion();
            Connection con = conn.conexion();
            try {
                PreparedStatement smtp = con.prepareStatement("DELETE FROM public.cliente WHERE \"cedulaCli\"= ? ;");
                smtp.setString(1, codigo);
                int count = smtp.executeUpdate();
                System.out.println("se a eliminado : " + count);
                table.removeRow(jTable2.getSelectedRow());
                JOptionPane.showMessageDialog(rootPane, "Se a eliminado correctamente");

            } catch (SQLException ex) {
                Logger.getLogger(Mesero.class.getName()).log(Level.SEVERE, null, ex);
            }

            System.out.println(codigo);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un renglon primero");

        }
    }//GEN-LAST:event_jButton3ActionPerformed

