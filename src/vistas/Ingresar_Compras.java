    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Object[] conn = con.Platos(null, (String) jComboBox1.getSelectedItem());
         
        rs = (ResultSet) conn[0];
        String msj = (String) conn[1];
        jTextArea1.append((String) jComboBox1.getSelectedItem() + " un costo total ");
        double cantidad = Integer.parseInt(jTextField1.getText());

        String cont = null;
        try {
            while (rs.next()) {
               
                cont = rs.getString("costos");
                
                cantidad = cantidad * Double.parseDouble(cont);
                comp =new Compra(rs.getString("id_plato"),String.valueOf (cantidad));
                
                lista.add(comp);
                ct=ct+cantidad;
                jTextArea1.append(cantidad+ "\n");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Ingresar_Compras.class.getName()).log(Level.SEVERE, null, ex);
        }
        jTextField1.setText(null);
        jComboBox1.setSelectedIndex(0);
        jTextArea1.setEditable(false);
        con.close();
    }//GEN-LAST:event_jButton1ActionPerformed

