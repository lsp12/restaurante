    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Conexion conn=new Conexion();
        String txtUser = this.txtUser.getText();
        String txtpass = new String(txtPass.getPassword());
        String categoria = (String) jComboBox2.getSelectedItem();
        

        try {
            boolean existe = false;
            
            ResultSet rs = conn.buscarLogin(txtUser, txtpass, categoria);
            while (rs.next()) {
                existe = true;
                id_empleado=rs.getString("id_empleado");
                JOptionPane.showMessageDialog(rootPane, "nombre:" + rs.getString("nombre"));
                JOptionPane.showMessageDialog(rootPane, "nombre:" + rs.getString("apellido"));
                JOptionPane.showMessageDialog(rootPane, "nombre:" + rs.getString("rol"));
            }
            if (!existe) {
                JOptionPane.showMessageDialog(rootPane, "contrase√±a o usuario incorrecta o inexistente");
                limpiar();
            }else{
                if(categoria=="mesero"){
                    Mesero me = new Mesero();
                    super.dispose();
                    me.setVisible(true);
                    
                }else if(categoria=="gerente"){
                    Gerente ge =new Gerente();
                    super.dispose();
                    ge.setVisible(true);
                    
                }
            }
            
            conn.close();
        } catch (SQLException ex) {
            System.out.println("error al buscar " + ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
 void limpiar(){
        this.txtUser.setText(null);
        this.txtPass.setText(null);
        jComboBox2.setSelectedIndex(0);
    }
