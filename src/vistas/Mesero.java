    void cargarTable() {
        Conexion conn = new Conexion();
        Connection con = conn.conexion();
        PreparedStatement smtp1;
        try {
            smtp1 = con.prepareStatement("SELECT * FROM cliente");
            ResultSet rs = smtp1.executeQuery();
            int cont = 1;
            while (rs.next()) {
                array[0] = String.valueOf(cont);
                array[1] = rs.getString("CedulaCli");
                array[2] = rs.getString("nombreCli");
                array[3] = rs.getString("apellidoCli");
                cont++;
                table.addRow(array);
            }
            con.close();
            rs.close();
            smtp1.close();
        } catch (SQLException ex) {
            Logger.getLogger(Mesero.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

