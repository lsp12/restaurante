    public void tablaM(){
        Object[] obs =con.CompraCli(Integer.valueOf(Mesero.id_cli));
        rs=(ResultSet) obs[0];
        String msj=(String) obs[1];
        JOptionPane.showMessageDialog(rootPane, msj);
        try {
            
            int cont = 1;
            while (rs.next()) {
                array[0] = String.valueOf(cont);
                array[1] = rs.getString("id_cliente");
                array[2] = rs.getString("valor");
                
                cont++;
                table.addRow(array);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Mesero.class.getName()).log(Level.SEVERE, null, ex);
        }
        con.close();
    }

