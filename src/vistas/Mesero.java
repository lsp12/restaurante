    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Conexion conn = new Conexion();
        Connection con = conn.conexion();

        String cedula = txtCedula.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();

        try {
            PreparedStatement smtp = con.prepareStatement("INSERT INTO public.cliente(\"cedulaCli\", \"nombreCli\", \"apellidoCli\") VALUES (?, ?, ?);");
            smtp.setString(1, cedula);
            smtp.setString(2, nombre);
            smtp.setString(3, apellido);
            int col = smtp.executeUpdate();

            int fila = jTable2.getRowCount();
            for (int i = fila - 1; i >= 0; i--) {
                table.removeRow(i);
            }

            smtp = con.prepareStatement("SELECT * FROM cliente");
            ResultSet rs = smtp.executeQuery();
            int cont = 1;
            while (rs.next()) {
                array[0] = String.valueOf(cont);
                array[1] = rs.getString("CedulaCli");
                array[2] = rs.getString("nombreCli");
                array[3] = rs.getString("apellidoCli");
                cont++;
                table.addRow(array);
            }
            smtp.close();
            rs.close();
            con.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

